import { readFileSync } from "node:fs";
import type { SummaryJson, PerTaskRow } from "./stats.ts";

/**
 * Render a summary.json into AUDIT-BENCHMARK.md.
 * Pure function: same input → same bytes.
 */
export function renderBenchmark(summaryPath: string): string {
  const raw = readFileSync(summaryPath, "utf-8");
  const summary: SummaryJson = JSON.parse(raw);

  const lines: string[] = [];

  lines.push("# AUDIT-BENCHMARK.md — Talking CLI Evidence Harness v0.5");
  lines.push("");
  lines.push("> Auto-generated from `summary.json`. Do not hand-edit. Fix the template instead.");
  lines.push("");

  if (summary.provider === "stub") {
    lines.push(
      "> ⚠️ **Stub data** — this artifact was generated by the internal stub provider and is not a live model-backed benchmark run.",
    );
    lines.push("");
  }

  // ─── Per-Task Results ──────────────────────────────────────────────────────
  lines.push("## Per-Task Results");
  lines.push("");
  lines.push(
    "| Task | Mute Pass | Talking Pass | Delta | Mute InTok | Talking InTok | Δ Input |",
  );
  lines.push("|------|-----------|--------------|-------|------------|---------------|---------|");
  for (const row of summary.perTask) {
    const mutePass = row.mute.pass ? "✅" : "❌";
    const talkingPass = row.talking.pass ? "✅" : "❌";
    const deltaIcon =
      row.delta === "talking_win" ? "↑ talking" : row.delta === "mute_win" ? "↓ mute" : "= tie";
    lines.push(
      `| ${row.taskId} | ${mutePass} | ${talkingPass} | ${deltaIcon} | ${row.mute.inputTokens} | ${row.talking.inputTokens} | ${row.deltaInputTokens > 0 ? "+" : ""}${row.deltaInputTokens} |`,
    );
  }
  lines.push("");

  // ─── Aggregate Stats ───────────────────────────────────────────────────────
  lines.push("## Aggregate Stats");
  lines.push("");
  const a = summary.aggregate;
  lines.push(`- **Talking wins**: ${a.talkingWins}`);
  lines.push(`- **Mute wins**: ${a.muteWins}`);
  lines.push(`- **Ties**: ${a.ties}`);
  lines.push("");
  lines.push("### Statistical Tests");
  lines.push("");
  lines.push(`- **Sign test**: n = ${a.signTestN}, p-value: ${a.signTestPValue.toFixed(4)}`);
  lines.push(`- **Wilcoxon signed-rank**: W = ${a.wilcoxonW}, p-value: ${a.wilcoxonPValue.toFixed(4)}`);
  lines.push("");
  lines.push(
    "> A p-value < 0.05 indicates a statistically significant difference between mute and talking variants.",
  );
  lines.push(
    "> p-value ≥ 0.05 means we cannot reject the null hypothesis (no measurable difference).",
  );
  lines.push("");

  // ─── Methodology ───────────────────────────────────────────────────────────
  lines.push("## Methodology");
  lines.push("");
  lines.push(
    "This document is generated by the internal benchmark harness. It is not part of the normal `talking-cli audit` / `audit-mcp` / `optimize` user workflow.",
  );
  lines.push("");
  lines.push(
    "This benchmark compares an identical benchmark executor running the same task set against two variants of the MCP filesystem server:",
  );
  lines.push("");
  lines.push("- **Mute**: unmodified upstream server, returning raw data only (no hints).");
  lines.push(
    "- **Talking**: same server, augmented with `hints` fields on empty-result / error paths.",
  );
  lines.push("");
  lines.push(
    "Each task runs against both variants. Success is defined as the agent completing the task's",
  );
  lines.push(
    "deterministic pass criterion (e.g. file exists with expected content, or expected answer emitted)",
  );
  lines.push("within ≤ 20 turns.");
  lines.push("");
  lines.push("### Statistical Approach");
  lines.push("");
  lines.push(
    "- **Primary analysis**: Paired sign test on pass/fail outcomes across task pairs.",
  );
  lines.push(
    "- **Secondary analysis**: Wilcoxon signed-rank test on per-task token/turn deltas.",
    "",
  );
  lines.push(
    "Both tests are non-parametric and suitable for small-N paired comparisons (N ≥ 4 for N=20 in production).",
  );
  lines.push("");

  // ─── Reproduction ──────────────────────────────────────────────────────────
  lines.push("## Reproduction");
  lines.push("");
  lines.push("### Product usage");
  lines.push("");
  lines.push("Normal use of `talking-cli` does **not** require any model API key.");
  lines.push("The following commands are local-first:");
  lines.push("");
  lines.push("- `talking-cli audit`");
  lines.push("- `talking-cli audit-mcp`");
  lines.push("- `talking-cli optimize`");
  lines.push("");
  lines.push("### Standalone lab benchmark mode");
  lines.push("");
  lines.push("To reproduce these results on a clean checkout:");
  lines.push("");
  lines.push("```bash");
  lines.push("# 1. Run the internal smoke harness (no model API key required)");
  lines.push("npm run benchmark:smoke");
  lines.push("");
  lines.push("# 2. Run the full benchmark once a model-backed provider is wired");
  lines.push("export ANTHROPIC_API_KEY=sk-ant-...   # example only; not required for talking-cli product usage");
  lines.push("npm run benchmark");
  lines.push("");
  lines.push("# 3. Inspect the generated benchmark artifacts");
  lines.push("dir benchmark/results/<date>");
  lines.push("```");
  lines.push("");
  lines.push("- **Executor**: `benchmark:smoke` currently uses a local stub executor; `benchmark` still expects a future model-backed standalone provider.");
  lines.push(
    "- **Server**: `@modelcontextprotocol/server-filesystem` vendored at pinned commit.",
  );
  lines.push(
    "- **Task set**: smoke mode runs 3 tasks today; production target remains the full 20+ task matrix from Phase 1.",
  );
  lines.push("");

  return lines.join("\n");
}
